    {
        $order = Order::find($id);
        $order->status = $request->status;
        $order->save();

        if($request->status == 4){
            ReceivedDate::create([
                'order_id' => $order->id,
                'received_by' => Auth::id()
            ]);
        }

        return response()->json($order, 200);
    }

